<#
.SYNOPSIS
Extracts CMake TRY_RUN and feature detection results from CMakeCache.txt files
and writes them as a CMake cache file that can be loaded via cmake -C.

.DESCRIPTION
This script is used by the CI pipeline to generate a tryrun cache after a
successful build, which is then persisted via Azure DevOps Cache@2.

.PARAMETER OutputFile
Path to write the generated CMake cache file.

.PARAMETER CacheDirs
One or more directories containing CMakeCache.txt files.

.EXAMPLE
.\extract-tryrun-cache.ps1 -OutputFile artifacts\cmake-tryrun-cache\tryrun.windows-x64.cmake -CacheDirs artifacts\obj
#>
param(
    [Parameter(Mandatory=$true)]
    [string]$OutputFile,

    [Parameter(Mandatory=$true, ValueFromRemainingArguments=$true)]
    [string[]]$CacheDirs
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

# Collect all CMakeCache.txt files from the specified directories
$cacheFiles = @()
foreach ($dir in $CacheDirs) {
    $direct = Join-Path $dir 'CMakeCache.txt'
    if (Test-Path $direct) {
        $cacheFiles += $direct
    }
    $found = Get-ChildItem -Path $dir -Recurse -Filter 'CMakeCache.txt' -ErrorAction SilentlyContinue
    foreach ($f in $found) {
        if ($f.FullName -ne (Resolve-Path $direct -ErrorAction SilentlyContinue)) {
            $cacheFiles += $f.FullName
        }
    }
}

if ($cacheFiles.Count -eq 0) {
    Write-Host 'Warning: No CMakeCache.txt files found in specified directories.'
    exit 0
}

Write-Host "Found $($cacheFiles.Count) CMakeCache.txt file(s)"

# Patterns to extract
$extractPattern = '^(HAVE_|HAS_|COMPILER_SUPPORTS_|ONE_SHARED_|PTHREAD_|REALPATH_|FNO_LTO_|LIBUNWIND_|BROTLI_)'

# Collect matching entries
$entries = @{}
foreach ($cacheFile in $cacheFiles) {
    Write-Host "  Processing: $cacheFile"
    foreach ($line in Get-Content $cacheFile) {
        if ($line -match '^#' -or $line -match '^//' -or [string]::IsNullOrWhiteSpace($line)) {
            continue
        }
        if ($line -match $extractPattern) {
            # Parse VARNAME:TYPE=VALUE — first occurrence wins
            $colonIdx = $line.IndexOf(':')
            if ($colonIdx -gt 0) {
                $varname = $line.Substring(0, $colonIdx)
                if (-not $entries.ContainsKey($varname)) {
                    $entries[$varname] = $line
                }
            }
        }
    }
}

$entryCount = $entries.Count
Write-Host "Extracted $entryCount unique cache entries"

if ($entryCount -eq 0) {
    Write-Host 'Warning: No matching cache entries found. Skipping output.'
    exit 0
}

# Create output directory
$outputDir = Split-Path -Parent $OutputFile
if (-not (Test-Path $outputDir)) {
    New-Item -ItemType Directory -Path $outputDir -Force | Out-Null
}

# Detect compiler version (cl.exe)
$compilerVersion = ''
try {
    $clOutput = & cl.exe 2>&1 | Select-Object -First 1
    if ($clOutput -match 'Version\s+(\d+)\.') {
        $compilerVersion = $Matches[1]
    }
} catch {
    # cl.exe not available
}

# Write header
$header = @'
# Auto-generated CMake tryrun cache for CI pipeline caching
#
# This file was generated by eng/native/extract-tryrun-cache.ps1 from
# CMakeCache.txt files after a successful build. It is cached by Azure DevOps
# Cache@2 and restored on subsequent runs to speed up CMake configure.
#
# DO NOT CHECK THIS FILE IN. It is generated dynamically by CI.
#
# To enable: set CLR_CMAKE_USE_PLATFORM_CACHE=1
#

# Helper macro for TRY_RUN results
macro(set_cache_value)
  set(${ARGV0} ${ARGV1} CACHE STRING "Result from TRY_RUN" FORCE)
  set(${ARGV0}__TRYRUN_OUTPUT "dummy output" CACHE STRING "Output from TRY_RUN" FORCE)
endmacro()

'@

Set-Content -Path $OutputFile -Value $header -NoNewline

if ($compilerVersion) {
    Add-Content -Path $OutputFile -Value "set(CLR_CMAKE_PLATFORM_CACHE_COMPILER_VERSION `"$compilerVersion`" CACHE STRING `"Compiler version for this cache`")"
    Add-Content -Path $OutputFile -Value ''
}

# Convert entries to cmake -C format
foreach ($line in ($entries.Values | Sort-Object)) {
    $colonIdx = $line.IndexOf(':')
    $varname = $line.Substring(0, $colonIdx)
    $rest = $line.Substring($colonIdx + 1)
    $eqIdx = $rest.IndexOf('=')
    $vartype = $rest.Substring(0, $eqIdx)
    $varvalue = $rest.Substring($eqIdx + 1)

    if ($varname -like '*_EXITCODE') {
        Add-Content -Path $OutputFile -Value "set_cache_value($varname $varvalue)"
    } elseif ($vartype -eq 'INTERNAL') {
        Add-Content -Path $OutputFile -Value "set($varname `"$varvalue`" CACHE INTERNAL `"`")"
    } else {
        Add-Content -Path $OutputFile -Value "set($varname `"$varvalue`" CACHE $vartype `"`")"
    }
}

Write-Host "Generated: $OutputFile ($entryCount entries)"
