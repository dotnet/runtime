#!/usr/bin/env bash
#
# Extracts CMake TRY_RUN and feature detection results from CMakeCache.txt files
# and writes them as a CMake cache file that can be loaded via cmake -C.
#
# This script is used by the CI pipeline to generate a tryrun cache after a
# successful build, which is then persisted via Azure DevOps Cache@2.
#
# Usage: extract-tryrun-cache.sh <output-file> <cmake-cache-dir>...
#
# Example:
#   extract-tryrun-cache.sh artifacts/cmake-tryrun-cache/tryrun.osx-x64.cmake \
#     artifacts/obj/coreclr/linux.x64.Release \
#     artifacts/obj/native/linux.x64.Release
#

set -euo pipefail

if [[ $# -lt 2 ]]; then
    echo "Usage: $0 <output-file> <cmake-cache-dir>..."
    echo "  <output-file>      Path to write the generated CMake cache file"
    echo "  <cmake-cache-dir>  One or more directories containing CMakeCache.txt"
    exit 1
fi

output_file="$1"
shift

# Collect all CMakeCache.txt files from the specified directories
cache_files=()
for dir in "$@"; do
    if [[ -f "$dir/CMakeCache.txt" ]]; then
        cache_files+=("$dir/CMakeCache.txt")
    fi
    # Also check subdirectories (e.g., artifacts/obj may have nested cmake builds)
    while IFS= read -r -d '' f; do
        cache_files+=("$f")
    done < <(find "$dir" -mindepth 1 -name CMakeCache.txt -print0 2>/dev/null)
done

if [[ ${#cache_files[@]} -eq 0 ]]; then
    echo "Warning: No CMakeCache.txt files found in specified directories."
    exit 0
fi

echo "Found ${#cache_files[@]} CMakeCache.txt file(s)"

# Patterns to extract from CMakeCache.txt
# These match the variables cached by check_*, try_run, and related macros
extract_pattern='^(HAVE_|HAS_|COMPILER_SUPPORTS_|ONE_SHARED_|PTHREAD_|REALPATH_|FNO_LTO_|LIBUNWIND_|BROTLI_)'

# Create output directory
mkdir -p "$(dirname "$output_file")"

# Get compiler version for the header
compiler_version=""
if command -v clang &>/dev/null; then
    compiler_version=$(clang --version 2>/dev/null | head -1 | sed -n 's/.*clang version \([0-9]*\)\..*/\1/p')
fi

# Temporary file for deduplication
tmpfile=$(mktemp)
trap 'rm -f "$tmpfile"' EXIT

# Extract matching entries from all CMakeCache.txt files
for cache_file in "${cache_files[@]}"; do
    echo "  Processing: $cache_file"
    # CMakeCache.txt format: VARNAME:TYPE=VALUE
    # Skip comments and empty lines
    grep -E "$extract_pattern" "$cache_file" 2>/dev/null | grep -v '^#' | grep -v '^//' >> "$tmpfile" || true
done

# Deduplicate (first occurrence wins)
sort -u -t: -k1,1 "$tmpfile" > "${tmpfile}.dedup"

# Count entries
entry_count=$(wc -l < "${tmpfile}.dedup" | tr -d ' ')
echo "Extracted $entry_count unique cache entries"

if [[ "$entry_count" -eq 0 ]]; then
    echo "Warning: No matching cache entries found. Skipping output."
    exit 0
fi

# Write the output cmake cache file
cat > "$output_file" << 'HEADER'
# Auto-generated CMake tryrun cache for CI pipeline caching
#
# This file was generated by eng/native/extract-tryrun-cache.sh from
# CMakeCache.txt files after a successful build. It is cached by Azure DevOps
# Cache@2 and restored on subsequent runs to speed up CMake configure.
#
# DO NOT CHECK THIS FILE IN. It is generated dynamically by CI.
#
# To disable: export CLR_CMAKE_SKIP_PLATFORM_CACHE=1
#

# Helper macro for TRY_RUN results
macro(set_cache_value)
  set(${ARGV0} ${ARGV1} CACHE STRING "Result from TRY_RUN" FORCE)
  set(${ARGV0}__TRYRUN_OUTPUT "dummy output" CACHE STRING "Output from TRY_RUN" FORCE)
endmacro()

HEADER

# Add compiler version if available
if [[ -n "$compiler_version" ]]; then
    echo "set(CLR_CMAKE_PLATFORM_CACHE_COMPILER_VERSION \"$compiler_version\" CACHE STRING \"Compiler version for this cache\")" >> "$output_file"
    echo "" >> "$output_file"
fi

# Convert CMakeCache.txt entries to cmake -C format
while IFS= read -r line; do
    # Parse: VARNAME:TYPE=VALUE
    varname=$(echo "$line" | sed 's/:.*//')
    typevalue=$(echo "$line" | sed 's/^[^:]*://')
    vartype=$(echo "$typevalue" | sed 's/=.*//')
    varvalue=$(echo "$typevalue" | sed 's/^[^=]*=//')

    # Handle TRY_RUN exitcode variables specially
    if [[ "$varname" == *_EXITCODE ]]; then
        echo "set_cache_value(${varname} ${varvalue})" >> "$output_file"
    elif [[ "$vartype" == "INTERNAL" ]]; then
        echo "set(${varname} \"${varvalue}\" CACHE INTERNAL \"\")" >> "$output_file"
    else
        echo "set(${varname} \"${varvalue}\" CACHE ${vartype} \"\")" >> "$output_file"
    fi
done < "${tmpfile}.dedup"

echo "Generated: $output_file ($entry_count entries)"
