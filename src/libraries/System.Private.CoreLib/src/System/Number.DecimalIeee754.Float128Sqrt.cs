    private static Float128 Float128Sqrt(Float128 x)
    {
        ulong msd = x._hi;
        ulong lsd = x._lo;

        // f' is the ux mantissa reinterpreted as a double in [1/2, 1).
        double f = BitConverter.UInt64BitsToDouble((msd >> 11) + ((ulong)(SqrtDoubleExponentBias - 2) << SqrtDoubleExponentPosition));

        int exponent = x._exponent;
        int parity = exponent & 1;
        exponent = (exponent + parity) >> 1;

        int shift = (64 + parity) - SqrtSinglePrecision;

        lsd = ((msd << (64 - shift)) | (lsd >> shift)) >> (64 - SqrtDoublePrecision);
        double fLo = (double)lsd;
        double fHi = ((double)(msd >> shift)) * SqrtReciprocalTwoPow24;
        fLo *= SqrtReciprocalTwoPow77;

        // The exponent parity is swapped into the table index so the reduced result lands in [1, 2).
        int index = ((int)(msd >> (64 - SqrtNumFractionBits - 1)) & 0xFF) ^ (parity << SqrtNumFractionBits);
        SqrtCoefficients p = s_sqrtTable[index];
        double g = (((double)p.A) * (f * f)) + ((((double)p.B) * f) + p.C);
        g *= SqrtRootTwo;

        f = fHi + fLo;
        double t = (float)(f * g);
        g = (float)g;

        double w = (1.0 - (t * g)) - ((((fHi * g) - t) + (fLo * g)) * g);
        double gLo = (g * (SqrtSevenEighths - ((SqrtThreeEighths * f) * (g * g)))) * w;

        ulong seed = (ulong)(long)(SqrtTwoPow24 * g);
        long correction = (long)(SqrtTwoPow75 * gLo);
        seed = (seed << 39) + ((ulong)(correction >> 12)) + ((ulong)((correction >> 11) & 1));
        ulong clamp = ((seed & (1UL << 62)) != 0) ? (UxMsb - 1) : ~0UL;
        seed = ((long)seed < 0) ? seed : clamp;

        Float128 s = new Float128(0, 1 - exponent, seed, 0);

        // One binary128 Newton iteration: result <- (s*x) * (3 - x*s^2) / 2.
        Float128Multiply(ref s, ref x, out Float128 sx);
        Float128 sCopy = s;
        Float128Multiply(ref sCopy, ref sx, out Float128 y);

        Span<Float128> diff = stackalloc Float128[1];
        Float128AddSub(UxThree, y, UxSub | UxNoNormalization, diff);
        y = diff[0];

        Float128Multiply(ref y, ref sx, out Float128 result);
        result._exponent -= 1;
        return result;
    }

    private static Float128 Float128Hypot(Float128 x, Float128 y)
    {
        Float128Multiply(ref x, ref x, out Float128 x2);
        Float128Multiply(ref y, ref y, out Float128 y2);

        Span<Float128> sum = stackalloc Float128[1];
        Float128AddSub(x2, y2, UxAdd, sum);

        Float128 s = sum[0];
        Float128Normalize(ref s);
        return Float128Sqrt(s);
    }
