using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;

namespace Microsoft.Diagnostics.DataContractReader.Generators;

/// <summary>
/// Source generator that discovers all <c>IData</c> implementations across
/// referenced assemblies and generates a static verification method that instantiates
/// each one against a noop Target. This enables load-time type validation without reflection.
/// </summary>
[Generator]
public class DataTypeVerificationGenerator : IIncrementalGenerator
{
    private const string IDataFullName = "Microsoft.Diagnostics.DataContractReader.Data.IData`1";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // Use the compilation to scan referenced assemblies for IData<T> implementations
        context.RegisterSourceOutput(context.CompilationProvider, Execute);
    }

    private static void Execute(SourceProductionContext context, Compilation compilation)
    {
        INamedTypeSymbol? idataInterface = compilation.GetTypeByMetadataName(IDataFullName);
        if (idataInterface is null)
            return;

        StringBuilder sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Diagnostics;");
        sb.AppendLine();
        sb.AppendLine("namespace Microsoft.Diagnostics.DataContractReader;");
        sb.AppendLine();
        sb.AppendLine("public static partial class DataDescriptorTypeVerification");
        sb.AppendLine("{");
        sb.AppendLine("    [Conditional(\"DEBUG\")]");
        sb.AppendLine("    static partial void VerifyAllDataTypes(Target verificationTarget)");
        sb.AppendLine("    {");

        // Scan all types in the compilation and referenced assemblies
        foreach (INamedTypeSymbol type in GetAllTypes(compilation))
        {
            if (type.IsAbstract || type.TypeKind != TypeKind.Class)
                continue;

            // Skip types not accessible from the consuming compilation
            if (!compilation.IsSymbolAccessibleWithin(type, compilation.Assembly))
                continue;

            // Check if this class implements IData<TSelf>
            bool implementsIData = type.AllInterfaces.Any(i =>
                i.IsGenericType &&
                SymbolEqualityComparer.Default.Equals(i.OriginalDefinition, idataInterface) &&
                SymbolEqualityComparer.Default.Equals(i.TypeArguments[0], type));

            if (!implementsIData)
                continue;

            string fullName = type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);

            sb.AppendLine($"        TryCreate<{fullName}>(verificationTarget);");
        }

        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    private static void TryCreate<T>(Target target) where T : global::Microsoft.Diagnostics.DataContractReader.Data.IData<T>");
        sb.AppendLine("    {");
        sb.AppendLine("        try { T.Create(target, default); }");
        sb.AppendLine("        catch (global::System.InvalidOperationException) { }");
        sb.AppendLine("        catch (global::System.Collections.Generic.KeyNotFoundException) { }");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        context.AddSource("DataDescriptorTypeVerification.g.cs", sb.ToString());
    }

    private static IEnumerable<INamedTypeSymbol> GetAllTypes(Compilation compilation)
    {
        // Get types from the current compilation
        foreach (INamedTypeSymbol type in GetTypesFromNamespace(compilation.GlobalNamespace))
            yield return type;

        // Get types from all referenced assemblies
        foreach (MetadataReference reference in compilation.References)
        {
            ISymbol? symbol = compilation.GetAssemblyOrModuleSymbol(reference);
            if (symbol is IAssemblySymbol assembly)
            {
                foreach (INamedTypeSymbol type in GetTypesFromNamespace(assembly.GlobalNamespace))
                    yield return type;
            }
        }
    }

    private static IEnumerable<INamedTypeSymbol> GetTypesFromNamespace(INamespaceSymbol ns)
    {
        foreach (INamedTypeSymbol type in ns.GetTypeMembers())
            yield return type;

        foreach (INamespaceSymbol childNs in ns.GetNamespaceMembers())
        {
            foreach (INamedTypeSymbol type in GetTypesFromNamespace(childNs))
                yield return type;
        }
    }
}
