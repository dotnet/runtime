namespace Microsoft.Diagnostics.DataContractReader.DataGenerator;

/// <summary>
/// Source emitted into each consuming assembly via
/// <c>RegisterPostInitializationOutput</c>. Contains the <c>LayoutPair</c>
/// struct (with a static <c>Resolve</c> factory) used by IData{T} ctors
/// to resolve fields across both a native cdac descriptor and a managed
/// type-metadata layout.
/// </summary>
/// <remarks>
/// Keeping these types generator-emitted-only (instead of shipping in
/// Abstractions) keeps the managed/native fallback machinery contained
/// inside the generator's surface -- consumers of the cdac data API don't
/// see any new public types, and the runtime conventions for fallback can
/// evolve without churning the abstraction layer.
/// </remarks>
internal static class PostInitSources
{
    public const string LayoutPairSourceHintName = "LayoutPair.g.cs";

    public const string LayoutPairNamespace = "Microsoft.Diagnostics.DataContractReader.Generated";

    public const string LayoutPairFullyQualifiedName = LayoutPairNamespace + ".LayoutPair";

    public const string LayoutPairSource = """
// <auto-generated/>
#nullable enable

using System;

namespace Microsoft.Diagnostics.DataContractReader.Generated;

/// <summary>
/// A pair of optional layout sources (native cdac descriptor + managed
/// type metadata) for an <see cref="IData{T}"/> class. Each field is
/// resolved by trying every candidate name against the native source
/// first, then against the managed source.
/// </summary>
/// <remarks>
/// The native source's field offsets are anchored at <c>address</c>; the
/// managed source's field offsets are anchored at
/// <c>address + ManagedDataOffset</c> (which is <c>Object.Size</c> for
/// managed reference types or <c>0</c> for value types).
/// </remarks>
internal readonly struct LayoutPair
{
    public LayoutPair(Target.TypeInfo? nativeType, Target.TypeInfo? managedType, ulong managedDataOffset)
    {
        NativeType = nativeType;
        ManagedType = managedType;
        ManagedDataOffset = managedDataOffset;
    }

    public Target.TypeInfo? NativeType { get; }
    public Target.TypeInfo? ManagedType { get; }
    public ulong ManagedDataOffset { get; }

    /// <summary>
    /// Size of the resolved instance, in bytes. Prefers the native source
    /// when both are present.
    /// </summary>
    public ulong InstanceSize
        => NativeType?.Size
           ?? ManagedType?.Size
           ?? throw new InvalidOperationException("Neither layout has a known instance size.");

    /// <summary>
    /// Tries to resolve the first listed name against the native descriptor,
    /// then the managed descriptor. Native wins on a tie.
    /// </summary>
    public bool TrySelect(TargetPointer address, out Target.TypeInfo type, out TargetPointer baseAddr, out string name, params ReadOnlySpan<string> names)
    {
        if (NativeType is Target.TypeInfo nt)
        {
            foreach (string candidate in names)
            {
                if (nt.Fields.ContainsKey(candidate))
                {
                    type = nt; baseAddr = address; name = candidate; return true;
                }
            }
        }
        if (ManagedType is Target.TypeInfo mt)
        {
            foreach (string candidate in names)
            {
                if (mt.Fields.ContainsKey(candidate))
                {
                    type = mt; baseAddr = address + ManagedDataOffset; name = candidate; return true;
                }
            }
        }
        type = default!; baseAddr = default; name = default!;
        return false;
    }

    /// <summary>
    /// Same as <see cref="TrySelect"/> but throws when no candidate matches.
    /// Used for required fields where a miss is a contract bug.
    /// </summary>
    public void Select(TargetPointer address, out Target.TypeInfo type, out TargetPointer baseAddr, out string name, params ReadOnlySpan<string> names)
    {
        if (!TrySelect(address, out type, out baseAddr, out name, names))
        {
            throw new InvalidOperationException(FormatMissing(names));
        }
    }

    private static string FormatMissing(ReadOnlySpan<string> names)
        => $"Field not found in any layout (names=[{string.Join(",", names.ToArray())}]).";

    /// <summary>
    /// Resolve native and/or managed layouts for an IData type. At least one of
    /// <paramref name="nativeName"/> or <paramref name="managedFullName"/> must
    /// resolve at runtime; if neither is available an
    /// <see cref="InvalidOperationException"/> is thrown.
    /// </summary>
    public static LayoutPair Resolve(Target target, string? nativeName, string? managedFullName, bool isValueType)
    {
        Target.TypeInfo? native = null;
        if (nativeName is not null && target.TryGetTypeInfo(nativeName, out Target.TypeInfo n))
            native = n;

        Target.TypeInfo? managed = null;
        ulong managedDataOffset = 0;
        if (managedFullName is not null
            && target.Contracts.ManagedTypeSource.TryGetTypeInfo(managedFullName, out Target.TypeInfo m))
        {
            managed = m;
            if (!isValueType)
            {
                Target.TypeInfo objType = target.GetTypeInfo("Object");
                managedDataOffset = objType.Size
                    ?? throw new InvalidOperationException(
                        "The 'Object' data descriptor must have a known Size to compute managed reference-type field offsets.");
            }
        }

        if (native is null && managed is null)
        {
            throw new InvalidOperationException(
                $"No descriptor available (native='{nativeName ?? "<none>"}', managed='{managedFullName ?? "<none>"}').");
        }

        return new LayoutPair(native, managed, managedDataOffset);
    }
}
""";
}
