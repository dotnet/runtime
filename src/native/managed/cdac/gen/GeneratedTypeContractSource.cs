namespace Microsoft.Diagnostics.DataContractReader.DataGenerator;

/// <summary>
/// Source for the <c>IGeneratedType</c> contract, its <c>GeneratedType_1</c>
/// implementation, and the <c>TargetExtensions.GetCachedLayoutSet</c> helper,
/// all emitted into each consuming assembly via <c>RegisterSourceOutput</c>
/// (gated by CompilationProvider to avoid duplicate symbols).
/// </summary>
/// <remarks>
/// The contract caches the result of <c>LayoutSet.Resolve(target, _typeNames)</c>
/// keyed by reference identity on the <c>_typeNames</c> array. The generator
/// emits exactly one static <c>_typeNames</c> per IData class, so reference
/// equality is the correct comparer and yields a 100% hit rate after the
/// first instantiation of each data class.
///
/// Registration happens lazily inside <c>GetCachedLayoutSet</c>: the first
/// call on a given <see cref="ContractRegistry"/> registers the
/// implementation; subsequent calls hit the cached contract instance.
/// Re-registering with the same lambda matches the no-op semantics of
/// <see cref="ContractRegistry.Register{TContract}"/>'s last-write-wins
/// behavior, so concurrent lazy registrations from multiple call sites are
/// safe.
/// </remarks>
internal static class GeneratedTypeContractSource
{
    public const string HintName = "GeneratedTypeContract.g.cs";

    public const string Namespace = "Microsoft.Diagnostics.DataContractReader.Generated";

    public const string FullyQualifiedName = Namespace + ".IGeneratedType";

    public const string Source = """
// <auto-generated/>
#nullable enable

using System;
using System.Collections.Generic;
using Microsoft.Diagnostics.DataContractReader;
using Microsoft.Diagnostics.DataContractReader.Contracts;

namespace Microsoft.Diagnostics.DataContractReader.Generated;

/// <summary>
/// Diagnostics-host-internal contract that caches per-target results of
/// <see cref="LayoutSet.Resolve(Target, string[])"/>, keyed by reference
/// identity on the generator-emitted static <c>_typeNames</c> array of each
/// IData class. Has no target-side data and is not advertised by the target's
/// contract descriptor; registered under
/// <see cref="ContractRegistry.Register{TContract}"/> with the empty-string
/// "default" version, which the registry falls back to when no target-declared
/// version exists.
/// </summary>
internal interface IGeneratedType : IContract
{
    static string IContract.Name => nameof(GeneratedType);

    LayoutSet GetOrAddLayoutSet(string[] typeNames);
}

/// <summary>
/// Marker type whose <c>nameof</c> supplies the contract name. Internal
/// contracts are not advertised in the target descriptor so this value is
/// purely diagnostic.
/// </summary>
internal static class GeneratedType { }

internal sealed class GeneratedType_1 : IGeneratedType
{
    private readonly Target _target;
    private readonly Dictionary<string[], LayoutSet> _cache =
        new(ReferenceEqualityComparer.Instance);

    public GeneratedType_1(Target target) => _target = target;

    public LayoutSet GetOrAddLayoutSet(string[] typeNames)
    {
        if (!_cache.TryGetValue(typeNames, out LayoutSet? cached))
        {
            cached = LayoutSet.Resolve(_target, typeNames);
            _cache[typeNames] = cached;
        }
        return cached;
    }

    public void Flush(FlushScope scope)
    {
        // LayoutSets resolve type info from contract descriptors and managed
        // metadata, both stable for the lifetime of a target snapshot and
        // immutable across FlushScope.ForwardExecution. Only clear on
        // FlushScope.All (matches a target re-attach / arbitrary snapshot).
        if (scope == FlushScope.All)
            _cache.Clear();
    }
}

/// <summary>
/// Extension helpers for code generated by the cdac source generator. Bridges
/// the generated IData constructors to the internal <see cref="IGeneratedType"/>
/// cache contract, performing first-use lazy registration so no global
/// registration step is required.
/// </summary>
internal static class TargetExtensions
{
    public static LayoutSet GetCachedLayoutSet(this Target target, string[] typeNames)
    {
        if (!target.Contracts.TryGetContract(out IGeneratedType contract))
        {
            // Register under the empty-string "default" version. The target
            // descriptor does not advertise IGeneratedType (it has no
            // target-side data), so the registry falls back to this entry.
            target.Contracts.Register<IGeneratedType>(string.Empty, static t => new GeneratedType_1(t));
            contract = target.Contracts.GetContract<IGeneratedType>();
        }
        return contract.GetOrAddLayoutSet(typeNames);
    }
}
""";
}
