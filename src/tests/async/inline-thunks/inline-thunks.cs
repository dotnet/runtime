using System.Runtime.CompilerServices;
using System.Threading.Tasks;
using Xunit;

public class InlineThunks
{
    // Test that the task-returning thunk of an async method can be inlined
    // into a non-async caller. The caller invokes the async method normally
    // (getting back a Task), so the JIT sees the task-returning thunk as the
    // callee. With a simplified thunk (no EH), this should be inlineable.
    [Fact]
    public static void TestInlineTaskReturningThunk()
    {
        int result = CallAsyncAndGetResult(10).GetAwaiter().GetResult();
        Assert.Equal(20, result);
    }

    [Fact]
    public static void TestInlineTaskReturningThunkVoid()
    {
        CallAsyncVoid().GetAwaiter().GetResult();
    }

    [Fact]
    public static void TestInlineValueTaskReturningThunk()
    {
        int result = CallAsyncValueTask(5).GetAwaiter().GetResult();
        Assert.Equal(10, result);
    }

    // Test that the async variant (the runtime-async body) of a small method
    // can be inlined into an async caller.
    [Fact]
    public static void TestInlineAsyncVariant()
    {
        int result = AsyncCallerOfSmallAsync(7).GetAwaiter().GetResult();
        Assert.Equal(14, result);
    }

    [Fact]
    public static void TestInlineAsyncVariantVoid()
    {
        AsyncCallerOfSmallAsyncVoid().GetAwaiter().GetResult();
    }

    [Fact]
    public static void TestInlineAsyncVariantValueTask()
    {
        int result = AsyncCallerOfSmallValueTaskAsync(3).GetAwaiter().GetResult();
        Assert.Equal(6, result);
    }

    // --- Task-returning thunk inlining targets ---
    // These are non-async methods whose bodies should be small enough
    // to inline the task-returning thunk of the async callee.
    // Each uses a dedicated callee to avoid poisoning the inline cache
    // for the async-variant tests below.

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static Task<int> CallAsyncAndGetResult(int x)
    {
        return SmallAsyncForThunk(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static Task CallAsyncVoid()
    {
        return SmallAsyncVoidForThunk();
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static ValueTask<int> CallAsyncValueTask(int x)
    {
        return SmallValueTaskAsyncForThunk(x);
    }

    // --- Async variant inlining targets ---
    // These are async methods that await small async methods.
    // The async variant (runtime-async body) of the callee should be inlineable.
    // Each uses a dedicated callee so the inline cache is not poisoned by
    // earlier task-returning thunk failures (CALLEE_AWAIT).

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task<int> AsyncCallerOfSmallAsync(int x)
    {
        return await SmallAsyncForVariant(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task AsyncCallerOfSmallAsyncVoid()
    {
        await SmallAsyncVoidForVariant();
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task<int> AsyncCallerOfSmallValueTaskAsync(int x)
    {
        return await SmallValueTaskAsyncForVariant(x);
    }

    // --- Non-runtime-async (state machine) inlining targets ---
    // These callers invoke traditional async state machine methods
    // (opted out of runtime async). The thunks generated by Roslyn
    // should still be inlineable.

    [Fact]
    public static void TestInlineNonRuntimeAsyncNotAwaited()
    {
        int result = CallNonRuntimeAsync(4).GetAwaiter().GetResult();
        Assert.Equal(8, result);
    }

    [Fact]
    public static void TestInlineNonRuntimeAsyncAwaited()
    {
        int result = AwaitNonRuntimeAsync(6).GetAwaiter().GetResult();
        Assert.Equal(12, result);
    }

    [Fact]
    public static void TestInlineNonRuntimeAsyncValueTaskNotAwaited()
    {
        int result = CallNonRuntimeValueTaskAsync(9).GetAwaiter().GetResult();
        Assert.Equal(18, result);
    }

    [Fact]
    public static void TestInlineNonRuntimeAsyncValueTaskAwaited()
    {
        int result = AwaitNonRuntimeValueTaskAsync(11).GetAwaiter().GetResult();
        Assert.Equal(22, result);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static Task<int> CallNonRuntimeAsync(int x)
    {
        return SmallNonRuntimeAsync(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task<int> AwaitNonRuntimeAsync(int x)
    {
        return await SmallNonRuntimeAsync(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static ValueTask<int> CallNonRuntimeValueTaskAsync(int x)
    {
        return SmallNonRuntimeValueTaskAsync(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task<int> AwaitNonRuntimeValueTaskAsync(int x)
    {
        return await SmallNonRuntimeValueTaskAsync(x);
    }

    // --- Small async methods for task-returning thunk tests ---
    // Only called from non-async callers (thunk inlining).

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static async Task<int> SmallAsyncForThunk(int x)
    {
        return x * 2;
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static async Task SmallAsyncVoidForThunk()
    {
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static async ValueTask<int> SmallValueTaskAsyncForThunk(int x)
    {
        return x * 2;
    }

    // --- Small async methods for async variant tests ---
    // Only called from async callers (variant inlining).

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static async Task<int> SmallAsyncForVariant(int x)
    {
        return x * 2;
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static async Task SmallAsyncVoidForVariant()
    {
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static async ValueTask<int> SmallValueTaskAsyncForVariant(int x)
    {
        return x * 2;
    }

    // --- Small async methods (non-runtime-async / traditional state machine) ---

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    [RuntimeAsyncMethodGeneration(false)]
    private static async Task<int> SmallNonRuntimeAsync(int x)
    {
        return x * 2;
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    [RuntimeAsyncMethodGeneration(false)]
    private static async ValueTask<int> SmallNonRuntimeValueTaskAsync(int x)
    {
        return x * 2;
    }

    // --- Cross-module async variant inlining ---
    // These async callers await small async methods from a separate assembly
    // (helper.dll). The async variant of the cross-module callee should be
    // inlineable, and the R2R image should contain a CheckILBody fixup.

    [Fact]
    public static void TestCrossModuleAsyncVariant()
    {
        int result = CrossModuleAsyncCaller(5).GetAwaiter().GetResult();
        Assert.Equal(15, result);
    }

    [Fact]
    public static void TestCrossModuleAsyncVariantVoid()
    {
        CrossModuleAsyncCallerVoid().GetAwaiter().GetResult();
    }

    [Fact]
    public static void TestCrossModuleAsyncVariantValueTask()
    {
        int result = CrossModuleAsyncCallerValueTask(4).GetAwaiter().GetResult();
        Assert.Equal(12, result);
    }

    [Fact]
    public static void TestCrossModuleTaskReturningThunk()
    {
        int result = CrossModuleCallAsync(8).GetAwaiter().GetResult();
        Assert.Equal(24, result);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task<int> CrossModuleAsyncCaller(int x)
    {
        return await CrossModuleHelper.SmallCrossModuleAsync(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task CrossModuleAsyncCallerVoid()
    {
        await CrossModuleHelper.SmallCrossModuleAsyncVoid();
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static async Task<int> CrossModuleAsyncCallerValueTask(int x)
    {
        return await CrossModuleHelper.SmallCrossModuleValueTaskAsync(x);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static Task<int> CrossModuleCallAsync(int x)
    {
        return CrossModuleHelper.SmallCrossModuleAsync(x);
    }

    // --- Cross-module sync baseline ---
    // Non-async cross-module inline as a baseline to verify cross-module
    // inlining infrastructure works independently of async.

    [Fact]
    public static void TestCrossModuleSyncBaseline()
    {
        int result = CrossModuleSyncCaller(7);
        Assert.Equal(21, result);
    }

    [MethodImpl(MethodImplOptions.NoInlining)]
    private static int CrossModuleSyncCaller(int x)
    {
        return CrossModuleHelper.SmallCrossModuleSync(x);
    }
}
